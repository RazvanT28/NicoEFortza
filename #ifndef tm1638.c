// create a program to sum 2 numbers and display the result on the display
#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a, b, c;
    printf("Enter the first number: ");
    scanf("%d", &a);
    printf("Enter the second number: ");
    scanf("%d", &b);
    c = a + b;
    printf("The sum of %d and %d is %d", a, b, c);
    return 0;
}
